    public void fillStudentsJtable(JTable table, String ValueToSearch) throws SQLException
    {
       PreparedStatement ps = null;
         Connection con;
         con = (java.sql.Connection) java.sql.DriverManager.getConnection("jdbc:mysql://localhost/attendence_ms","root","");
            ps = con.prepareStatement("SELECT * FROM `studentb` WHERE CONCAT('Name','FName','Contact','Dept','Sem','Address','Email')LIKE ?");
         ps.setString(1, "%"+ValueToSearch+"%");
         ResultSet rs = ps.executeQuery();
         DefaultTableModel model = (DefaultTableModel)table.getModel();
         Object[] row;
         while(rs.next())
         {
             row = new Object[8];
             row[0] = rs.getInt(1);
             row[1] = rs.getString(2);
             row[2] = rs.getString(3);
             row[3] = rs.getString(4);
             row[4] = rs.getString(5);
             row[5] = rs.getString(6);
             row[6] = rs.getString(7);
             row[7] = rs.getString(8);
             model.addRow(row);
             
         }
    }

