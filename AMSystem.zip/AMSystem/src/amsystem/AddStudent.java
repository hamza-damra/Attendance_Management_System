                  String email = txtEmail.getText();
                 
                  
             
                     Class.forName("com.mysql.cj.jdbc.Driver");
                  Connection con = (java.sql.Connection) java.sql.DriverManager.getConnection("jdbc:mysql://localhost/attendence_ms","root","");
                 PreparedStatement ps =con.prepareStatement("insert into studentb(id, Name, fName, Contact, Dept, Sem, Address, Email) value(?,?,?,?,?,?,?,?)");  
                 ps.setInt(1, id);
                 ps.setString(2, name);
                 ps.setString(3, fname);
                 ps.setInt(4, contact);
                 ps.setString(5, dept);
                 ps.setString(6, sem);
                  ps.setString(7, address);
